package sana;

public class vehicle {

	private String vehicleNumber;
	private String colour;
	private String model;
	private String capacity;
	private String type;
	private int maufactureYear;
	private int chassiNumber;
	private int passengerCapacity;
	private int milage;
	private String fueltype;

	public void setVehicalNumber(String vehicleNumber) {
		this.vehicleNumber = vehicleNumber;
	}

	public String getVehicalNumber() {
		return vehicleNumber;
	}

	public void setColour(String colour) {
		this.colour = colour;
	}

	public String getcolour() {
		return colour;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getmodel() {
		return model;
	}

	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}

	public String getcapacity() {
		return capacity;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getMaufactureYear() {
		return maufactureYear;
	}

	public void setMaufactureYear(int maufactureYear) {
		this.maufactureYear = maufactureYear;
	}

	public int getChassiNumber() {
		return chassiNumber;
	}

	public void setChassiNumber(int chassiNumber) {
		this.chassiNumber = chassiNumber;
	}

	public int getPassengerCapacity() {
		return passengerCapacity;
	}

	public void setPassengerCapacity(int passengerCapacity) {
		this.passengerCapacity = passengerCapacity;
	}

	public int getMilage() {
		return milage;
	}

	public void setMilage(int milage) {
		this.milage = milage;
	}

	public String getFueltype() {
		return fueltype;
	}

	public void setFueltype(String fueltype) {
		this.fueltype = fueltype;
	}

}
